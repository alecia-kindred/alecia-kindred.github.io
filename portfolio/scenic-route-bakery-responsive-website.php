	<h2>Accessibility Considerations</h2>
	<p>Throughout the design process, the following accessibility considerations were made to ensure the app could be easily used by all.</p>
	<div class="flex">
		<div class="consideration"><span>1</span> Alternative options to using gestures are provided and gestures (such as swiping) are not the only way to complete a task</div>
		<div class="consideration"><span>2</span> Colors meet the required color contrasts to be accessible based on WCAG standards</div>
		<div class="consideration"><span>3</span> Graphical cues are provided to guide non-literate users and people with cognitive disabilities by combining text with icons and images</div>
	</div>
	<br>
	<h2>Takeaways</h2>
	<p>Throughout this process I learned to keep the user the focus of my designs and that making designs accessible benefits everyone. I also learned that my first idea is not always the best idea and feedback only helps me become a better designer.</p>
