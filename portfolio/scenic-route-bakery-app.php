	<p>Over the course of 3 months, I completed courses two through five for the Google UX Design Professional Certificate in which I learned and followed the UX design process, which included user research, creating paper wireframes, digital wireframes, and a low-fidelity prototype, conducting a usability study, and finally creating mockups and a high-fidelity prototype. During these courses I also learned a new tool: Figma.</p>
