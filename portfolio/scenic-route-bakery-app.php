	<h2>Usability Study</h2>
	<p>During the usability study, users had some confusion navigating the app, though all were able to successfully complete the assigned tasks. Below are some of the usability study findings.</p>
	<div class="flex">
		<div class="finding"><span>1</span> There was no option to save while editing an item</div>
		<div class="finding"><span>2</span> The navigation was confusing and hard to find the menu</div>
		<div class="finding"><span>3</span> There were too many clicks to find menu items</div>
	</div>
	<br>
	<h2>Mockups</h2>
	<p>Mockups were created in Figma as a representation of the final app design. The homepage design was updated based on findings from the usability study to include tabs as an easier way for customers to access the full menu. New pages were also created that had been missing in the wireframe creation.</p>
